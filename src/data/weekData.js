// ── Skeleton week builder ─────────────────────────────────────────────────────
function sk(weekNumber, dateRange, title, scriptureRange, summary, lulId, nextTitle) {
  return {
    weekNumber, year: 2026, dateRange, title, scriptureRange,
    theme: summary, skeleton: true,
    days: [
      { day: 0, label: "Sunday", shortLabel: "Sun", title: "Overview & Invitation", timeEst: "10 min",
        content: [
          { type: "intro", icon: "📖", label: "This Week's Focus",
            text: `This week: ${scriptureRange} — ${summary}. Use Sunday to read the overview and set your intention.` },
          ...(lulId ? [{ type: "lineUponLine", icon: "🎥", label: "Line Upon Line Overview",
            title: `${scriptureRange} — Come Follow Me Overview`,
            description: `Line Upon Line's weekly CFM video for ${scriptureRange}.`,
            embedId: lulId }] : []),
          { type: "coming", icon: "🔄", label: "Full Content Coming",
            text: `Full daily content for this week can be generated using the Update button in ⚙️ Settings → Generate Week Content.` },
        ] },
      ...["Monday","Tuesday","Wednesday","Thursday","Friday"].map((label, i) => ({
        day: i+1, label, shortLabel: label.slice(0,3),
        title: `Day ${i+1} Study`, timeEst: "10–15 min",
        content: [
          { type: "scripture", icon: "📜", label: "Scripture Reading",
            reference: scriptureRange, text: `Continue your reading in ${scriptureRange}.`,
            url: `gospellibrary://content/scriptures/ot`,
            webUrl: `https://www.churchofjesuschrist.org/study/scriptures/ot?lang=eng` },
          { type: "coming", icon: "🔄", label: "Full Content Coming",
            text: `Generate full daily content using the Update button in ⚙️ Settings.` },
        ] })),
      { day: 6, label: "Saturday", shortLabel: "Sat", title: "Week Reflection", timeEst: "10 min",
        content: [
          { type: "review", icon: "🔄", label: "Week in Review",
            text: `Reflect on what you studied this week in ${scriptureRange}. What stood out? What will you apply?` },
          { type: "question", icon: "💭", label: "Personal Reflection",
            text: `What principle from ${scriptureRange} do you want to carry into next week?`, mode: "personal" },
          { type: "question", icon: "👨‍👩‍👧", label: "Family Discussion",
            text: `As a family, share one thing each person learned this week from ${scriptureRange}.`, mode: "family" },
          ...(nextTitle ? [{ type: "nextweek", icon: "➡️", label: "Coming Next Week",
            text: `Next week: ${nextTitle}` }] : []),
        ] },
    ],
  };
}

